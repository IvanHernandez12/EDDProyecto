    public void ponerPacientesOncoEnLaTabla()
    {
        modeloTabla = (DefaultTableModel) jTable1.getModel();
        if (LogicaNegocioJose.pacientesEnEspera != null)
        {
            NodoML nodoPaciente = LogicaNegocioJose.pacientesEnEspera.eliminaNodoPacienteOncologia();
            modeloTabla.setRowCount(0);
            if (nodoPaciente == null)
            {
                JOptionPane.showMessageDialog(null, "no hay pacientes en oncologia");
            }
            while (nodoPaciente != null)
            {
                Paciente paciente = (Paciente) nodoPaciente.getObj();

                modeloTabla.addRow(new Object[]
                {
                    paciente.getClaveP(),
                    paciente.getNombre(),
                    paciente.getSexo(),
                    paciente.getEstatus(),
                    paciente.getVigencia()
                });
                pacientesEliminado.insertaPacienteOncologia(nodoPaciente);
                nodoPaciente = LogicaNegocioJose.pacientesEnEspera.eliminaNodoPacienteOncologia();

            }
            jTable1.setModel(modeloTabla);
            nodoPaciente = pacientesEliminado.eliminaNodoPacienteOncologia();
            while (nodoPaciente != null)
            {

                LogicaNegocioJose.pacientesEnEspera.insertaPacienteOncologia(nodoPaciente);
                nodoPaciente = pacientesEliminado.eliminaNodoPacienteOncologia();
            }
        }

    }

